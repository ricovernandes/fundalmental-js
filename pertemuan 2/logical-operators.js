// logical operators

const statement1 = 5 > 3 // true
const statement2 = 40 > 3 // false

// OR operator
console.log(statement1 || statement2) // true

// AND operator
console.log(statement1 && statement2) // false

// NOT operator
console.log(!statement1) // false
console.log(!statement2) // true


// comparison operators akan menghasikan boolean
// comparison operators ga berlaku utk tipe data 'object'

// == perbandingan tanpa melihat tipe data
console.log(5 == 5) // true
console.log(5 == '5') // true

// === perbandingan melihat tipe data
console.log(5 === 5) // true
console.log(5 ==='5') // false

// > lebih dari
console.log(5 >  3) // true

// < kurang dari 
console.log(5 < 3) // false

// >= lebih dari sama dengan
console.log(5 >= 5) // true

// <= kurang dari sama dengan
console.log(6 <= 10) // true



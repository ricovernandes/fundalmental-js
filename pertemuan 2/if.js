// "if - else if - else" accepts boolean

const name = 'budi'

// basic if statement
if (name === 'rico') {
  console.log('hello rico')
} else {
  console.log('hello stranger')
}

// if - else if statement
if (name === 'rico') {
  console.log('hello rico')
} else if (name === 'budi') {
  console.log('hello budi')
} else {
  console.log('hello stanger')
}